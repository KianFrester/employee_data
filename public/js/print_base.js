// public/js/print_base.js
window.printWithBolinaoHeader = async function ({
  title = "Print",
  tableHTML = "",
  headerColor = "#0d6efd",
}) {
  const base = (window.ASSET_BASE || "").replace(/\/$/, "");
  const newWin = window.open("about:blank", "_blank");
  if (!newWin) return;

  const wait = (ms) => new Promise((r) => setTimeout(r, ms));

  async function preloadImages(win) {
    const imgs = Array.from(win.document.images || []);
    if (!imgs.length) return;

    await Promise.all(
      imgs.map(async (img) => {
        try {
          const src = img.getAttribute("src");
          img.setAttribute("src", src);

          if (!img.complete) {
            await new Promise((res) => {
              img.onload = img.onerror = () => res();
            });
          }

          if (img.decode) {
            try { await img.decode(); } catch {}
          }
        } catch {}
      })
    );

    await wait(150);
    await wait(150);
  }

  newWin.document.open();
  newWin.document.write(`
<!DOCTYPE html>
<html>
<head>
  <meta charset="utf-8">
  <title>${title}</title>

  <link href="https://cdn.jsdelivr.net/npm/bootstrap@5.3.1/dist/css/bootstrap.min.css" rel="stylesheet">

  <style>
    body { font-family: Arial, sans-serif; }

    table {
      width: 100%;
      border-collapse: collapse;
      margin-bottom: 20px;
    }

    th, td {
      border: 1px solid #000;
      padding: 6px;
      text-align: center;
      font-size: 12px;
      vertical-align: middle;
    }

    th {
      background: ${headerColor};
      color: #fff;
    }

    .print-header {
      display: flex;
      align-items: center;
      justify-content: space-between;
      gap: 12px;
      margin-bottom: 10px;
    }

    .logo {
      height: 100px;
      width: auto;
      object-fit: contain;
    }

    .header-text {
      text-align: center;
      flex: 1;
      line-height: 1.2;
    }

    .header-text h1 {
      font-size: 18px;
      margin: 4px 0;
      font-weight: bold;
    }

    .header-text p {
      margin: 2px 0;
      font-size: 12px;
    }

    .print-footer {
      text-align: center;
      margin-top: 30px;
      font-size: 12px;
      color: #777;
    }

    @media print {
      body {
        -webkit-print-color-adjust: exact;
        print-color-adjust: exact;
      }
    }
  </style>
</head>

<body>

  <div class="print-header">
    <img src="${base}/assets/images/bagong_pilipinas.png" class="logo">

    <div class="header-text">
      <p>Republic of the Philippines</p>
      <p>Province of Pangasinan</p>
      <h1>MUNICIPALITY OF BOLINAO</h1>
      <p>Date: ${new Date().toLocaleDateString()}</p>
    </div>

    <img src="${base}/assets/images/bolinao_logo.png" class="logo">
  </div>

  <hr style="border-top:1px solid #3e3e3e; margin-bottom:20px;">

  ${tableHTML}

  <div class="print-footer">
    <p>Generated by Municipality of Bolinao</p>
  </div>

</body>
</html>
  `);
  newWin.document.close();

  newWin.addEventListener("load", async () => {
    await preloadImages(newWin);
    newWin.focus();
    newWin.print();
  });
};
